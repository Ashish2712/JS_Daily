// A stack is a Last In, First Out (LIFO) data structure. 
// You can think of it like a stack of plates. The last plate you put on the stack is the first one to be removed.


let stack = [];
stack.push(1); // Push an element onto the stack
stack.push(2);
stack.pop();   // Pop the last element (removes 2)

console.log(stack); // [1]
